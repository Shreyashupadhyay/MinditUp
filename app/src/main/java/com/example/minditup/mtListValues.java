package com.example.minditup;

public class mtListValues {
    String status,reason,date,time;

    public mtListValues(String status, String reason, String date, String time) {
        this.status = status;
        this.reason = reason;
        this.date = date;
        this.time = time;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
